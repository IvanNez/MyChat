import Foundation
import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return UIColor(red: 208/255.0, green: 2/255.0, blue: 27/255.0, alpha: 1/1.0)
    }
    
    static func mainWhite() -> UIColor {
        return UIColor(red: 247/255.0, green: 248/255.0, blue: 253/255.0, alpha: 1/1.0)
    }
    
    static func buttonDark() -> UIColor {
        return UIColor(red: 51/255.0, green: 51/255.0, blue: 51/255.0, alpha: 1/1.0)
    }
    
}
